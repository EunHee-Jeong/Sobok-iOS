import Foundation

extension Date {
    enum FormatType {
        case full
        case year
        case month
        case time
        
        var description: String {
            switch self {
            case .full:
                return ""
            case .year:
                <#code#>
            case .month:
                <#code#>
            case .time:
                <#code#>
            }
        }
    }
}
